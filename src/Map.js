const TILE_TYPE = { NORMAL: "NORMAL", HOLE: "HOLE", RESOURCE: "RESOURCE" };
const TILES = [TILE_TYPE.NORMAL, TILE_TYPE.HOLE, TILE_TYPE.RESOURCE];
const TILE_COLORS = {
  NORMAL: (cell) => (cell ? "#ddd" : "#e6e6e6"),
  HOLE: (_) => "#333",
  RESOURCE: (_) => "#acf",
};

class Map {
  constructor() {
    this.grid = m;
  }

  tick() {}

  coordsToTile(x, y) {
    return [Math.floor(x / Map.tileSize), Math.floor(y / Map.tileSize)];
  }

  tileToCoords(x, y) {
    return [
      x * Map.tileSize + Map.tileSize / 2,
      y * Map.tileSize + Map.tileSize / 2,
    ];
  }

  draw(drawer) {
    this.grid.forEach((row, y) => {
      let normalColor = !!(y % 2);
      row.forEach((cell, x) => {
        normalColor = !normalColor;
        const tileType = TILES[cell];
        const colorFunc = TILE_COLORS[tileType];
        const color = colorFunc(normalColor);
        drawer.rect({
          fillColor: color,
          rect: [
            x * Map.tileSizeX,
            y * Map.tileSizeY,
            Map.tileSizeX,
            Map.tileSizeY,
          ],
        });
      });
    });
  }
}

Map.mapAdjust = (x, y) => [
  (x * Map.tileSizeX) / Map.tileSize,
  (y * Map.tileSizeY) / Map.tileSize,
];

Map.size = 4000;
Map.tileSize = 80;
Map.tileSizeX = 80;
Map.tileSizeY = 80;

export default Map;
