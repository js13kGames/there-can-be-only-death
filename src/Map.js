export const TILE_TYPE = { NORMAL: "NORMAL", HOLE: "HOLE", MINE: "MINE" };
export const TILES = [TILE_TYPE.NORMAL, TILE_TYPE.HOLE, TILE_TYPE.MINE];

class Map {
  constructor() {
    this.grid = m;
  }

  tick() {}

  coordsToTile(x, y) {
    return [Math.floor(x / Map.tileSize), Math.floor(y / Map.tileSize)];
  }

  tileToCoords(x, y) {
    return [
      x * Map.tileSize + Map.tileSize / 2,
      y * Map.tileSize + Map.tileSize / 2,
    ];
  }

  draw(drawer) {
    this.grid.forEach((row, y) => {
      let normalColor = !!(y % 2);
      row.forEach((cell, x) => {
        normalColor = !normalColor;
        const tileType = TILES[cell];
        const color =
          tileType === TILE_TYPE.HOLE
            ? "#333"
            : normalColor
            ? "#ddd"
            : "#e6e6e6";
        drawer.rect({
          fillColor: color,
          rect: [
            x * Map.tileSizeX,
            y * Map.tileSizeY,
            Map.tileSizeX,
            Map.tileSizeY,
          ],
        });
      });
    });
  }
}

Map.mapAdjust = (x, y) => [
  (x * Map.tileSizeX) / Map.tileSize,
  (y * Map.tileSizeY) / Map.tileSize,
];

Map.size = 4000;
Map.tileSize = 80;
Map.tileSizeX = 80;
Map.tileSizeY = 80;

export default Map;
