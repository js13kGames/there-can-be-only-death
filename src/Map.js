const TILE_TYPE = { NORMAL: "NORMAL", HOLE: "HOLE", RESOURCE: "RESOURCE" };
const TILES = [TILE_TYPE.NORMAL, TILE_TYPE.HOLE, TILE_TYPE.RESOURCE];
const TILE_COLORS = {
  NORMAL: (cell) => (cell ? "#ddd" : "#e6e6e6"),
  HOLE: (_) => "#333",
  RESOURCE: (_) => "#acf",
};

class Map {
  constructor() {
    this.map = m;
  }

  tick() {}

  draw(drawer) {
    this.map.forEach((row, y) => {
      let normalColor = !!(y % 2);
      row.forEach((cell, x) => {
        normalColor = !normalColor;
        const tileType = TILES[cell];
        const colorFunc = TILE_COLORS[tileType];
        const color = colorFunc(normalColor);
        drawer.rect({
          fillColor: color,
          rect: [
            x * Map.tileSizeX,
            y * Map.tileSizeY,
            Map.tileSizeX,
            Map.tileSizeY,
          ],
        });
      });
    });

    // minimap
    const minimapSize = 200;
    drawer.rect({
      adjusted: false,
      fillColor: "#eee",
      rect: [
        drawer.width - minimapSize,
        drawer.height - minimapSize,
        minimapSize,
        minimapSize,
      ],
    });

    const x =
      drawer.width -
      minimapSize +
      (drawer.camera.position_x / Map.size) * minimapSize;
    const y =
      drawer.height -
      minimapSize +
      (drawer.camera.position_y / ((Map.tileSizeY / Map.tileSize) * Map.size)) *
        minimapSize;
    drawer.rect({
      adjusted: false,
      strokeColor: "#333",
      rect: [
        x,
        y,
        (drawer.width / Map.size) * minimapSize,
        (drawer.height / ((Map.tileSizeY / Map.tileSize) * Map.size)) *
          minimapSize,
      ],
    });
  }
}

Map.mapAdjust = (x, y) => [
  (x * Map.tileSizeX) / Map.tileSize,
  (y * Map.tileSizeY) / Map.tileSize,
];

Map.size = 4000;
Map.tileSize = 80;
Map.tileSizeX = 80;
Map.tileSizeY = 80;

export default Map;
